    private void clearText() {
       jpfConfirmPassword.setText("");
       jpfNewPassword.setText("");
       jpfNewPassword.requestFocus();
    }

    private boolean checkNewPassword() {
        boolean result = true;
        String newPassword = new String(jpfNewPassword.getPassword());
        if(!newPassword.equals("")){
            result = false;
        }
        return result;
    }

    private boolean checkConfirmPassword() {
        boolean result = true;
        String confirmPassword = new String(jpfConfirmPassword.getPassword());
        if(!confirmPassword.equals("")){
            result = false;
        }
        return result;
    }

    private boolean checkSamePassword() {
        boolean result = false;
        String newPassword = new String(jpfNewPassword.getPassword());
        String confirmPassword = new String(jpfConfirmPassword.getPassword());
        if(newPassword.equals(confirmPassword))
            result = true;
        
        return result;
    }
