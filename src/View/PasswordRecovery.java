    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
        boolean checkNewPasswordEmpty = checkNewPassword();
        boolean checkConfirmPasswordEmpty = checkConfirmPassword();
        
        if(checkNewPasswordEmpty&&checkConfirmPasswordEmpty)
            JOptionPane.showMessageDialog(this, "Please enter your new password and confirm password");
        else if(checkNewPasswordEmpty)
            JOptionPane.showMessageDialog(this, "Please enter your new password");
        else if(checkConfirmPasswordEmpty)
            JOptionPane.showMessageDialog(this, "Please enter your confirm password");
        else{
            boolean checkSamePassword = checkSamePassword();
           if(checkSamePassword){
                String newPassword = new String(jpfConfirmPassword.getPassword());
            for(int i=1;i<=HomePage.HRData.size();i++){
                if(username.equals(HomePage.HRData.get(i).getUsername())){
                    HomePage.HRData.get(i).setPassword(newPassword);
                    break;
                }
            }
        JOptionPane.showMessageDialog(this, "Your password recovery was successed");
        dispose();
           }
           else
               JOptionPane.showMessageDialog(this, "Your password are not matched\nPlease enter the same password");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

