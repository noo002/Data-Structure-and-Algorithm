    public int getSum() {
        DefaultTableModel model2 = (DefaultTableModel)jTable1.getModel();
        int rowsCount = jTable1.getRowCount();
        int sum = 0;
        for(int i = 0; i < rowsCount; i++) //calculate total
        {
            sum += Double.parseDouble(jTable1.getValueAt(i, 3).toString());
        }
        return sum;
    }
    

